/*
	常量 
		A.字符串常量 双引号 ""
		B.整数常量   12， -3
		C.小数常量   0.12， -0.8
		D.字符常量   单引号 ''
		E.布尔常量   true, false
		F.空常量     null
*/


public class Const {
	public static void main(String[] args) {
		// 字符串常量
		System.out.println("Hello, World!");
		
		// 整数常量
		System.out.println(100);

		// 小数常量
		System.out.println(12.34);

		// 字符常量
		System.out.println('A');

		// 布尔常量
		System.out.println(true);
		System.out.println(false);

	}
}